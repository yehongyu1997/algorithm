// d定义一个累加器
var count =0;
// 定义一个空字符串
var str ='';
for (var i=100;i<=1000;i++){  
    // 满足被5或者6整出的数
    if(i%5==0||i%6==0){
        // 字符串拼接
        str=str+i+' ';
        // 累加器自加
        count++; 
        // 当累加器自增到10的时候输出当前字符串
        if(count%10==0){
          console.log(str);
        //   此条语句表示开始拼接新的字符串
          str='';
        }
    }
}

